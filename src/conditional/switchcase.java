package conditional;

public class switchcase {
    public static void main(String[] args) {
        int number = 3;
        switch (number){
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("burger");
                break;
            case 3:
                System.out.println("Momos");
                break;
            default:
                System.out.println("its a wrong realization");
        }
    }
}
